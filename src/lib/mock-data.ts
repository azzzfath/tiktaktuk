function buildSeats(): Seat[] {
  const seats: Seat[] = [];
  const rows = ["A", "B", "C", "D", "E"];
  const categoriesByRow: Record<string, string> = {
    A: "cat_vvip",
    B: "cat_vip",
    C: "cat_1",
    D: "cat_1",
    E: "cat_2",
  };
  rows.forEach((row) => {
    for (let i = 1; i <= 8; i++) {
      const id = `${row}${i}`;
      const isUnavailable = (row === "A" && (i === 3 || i === 7)) || (row === "C" && i === 5);
      seats.push({
        id,
        label: id,
        categoryId: categoriesByRow[row],
        available: !isUnavailable,
      });
    }
  });
  return seats;
}

export function findCategory(id: string): TicketCategory | undefined {
  return mockEvent.categories.find((c) => c.id === id);
}

